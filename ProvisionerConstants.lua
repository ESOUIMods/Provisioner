        isOk = true
    end

    return isOk
end

-- Arguments Required: NodeName
-- Returns -1 when Object interacted with is invalid
-- Valid types: (1)Mining, (2)Clothing, (3)Enchanting
-- (4)Alchemy, (5)Wood, (7)Solvents
-- Containers are assigned 4 Alchemy the same as 2.2
function COOK.GetProfessionTypeOnUpdate(name)
    local tsId

    if COOK.IsValidSolvent(name) then
        tsId = 7
        return tsId
    end

    if COOK.IsValidMiningOnUpdate(name) then
        tsId = 1
        return tsId
    end

    if COOK.IsValidClothingOnUpdate(name) then
        tsId = 2
        return tsId
    end

    if COOK.IsValidEnchantingOnUpdate(name) then
        tsId = 3
        return tsId
    end

    if COOK.IsValidAlchemyOnUpdate(name) then
        tsId = 4
        return tsId
    end

    if COOK.IsValidWoodworkingOnUpdate(name) then
        tsId = 5
        return tsId
    end

    if COOK.IsValidContainerOnUpdate(name) then
        tsId = 4
        return tsId
    end

    return -1
end

-- Arguments Required ItemID, NodeName
-- Returns -1 when Object interacted with is invalid
-- Valid types: (1)Mining, (2)Clothing, (3)Enchanting
-- (4)Alchemy, (5)Wood, (6)Chests, (7)Solvents
-- (8)Fish
function COOK.GetProfessionType(id, name)
    local tsId
    id = tonumber(id)

    if COOK.defaults.verbose then
        d("Attempting GetProfessionType with id : " .. id)
        d("Node Name : " .. name)
    end

    --if COOK.IsValidSolvent(name) then
    --    tsId = 7
    --    if COOK.settings.verbose then
    --        d("Solvent id assigned : " .. tsId)
    --    end
    --    return tsId
    --end

    -- For this HarvestMap version there are no containers
    -- Set any container found to 0 so that it is not recorded.
    if COOK.IsValidContainer(name) then
        tsId = 0
        if COOK.defaults.verbose then
            d("Container is not used in this version id assigned : " .. tsId)
        end
        return tsId
    end

    -- if no valid Node Name by Name is found use ItemID
    for key1, tsData in pairs(COOK.professions) do
        for key2, value in pairs(tsData) do
            if value == id then
                tsId = key1
                if COOK.defaults.verbose then
                    d("Esohead id assigned : " .. tsId)
                end
                return tsId
            end
        end
    end

    if COOK.defaults.verbose then
        d("No Profession Type found with id : " .. id)
        d("In GetProfessionType with name : " .. name)
    end

    return -1
end

